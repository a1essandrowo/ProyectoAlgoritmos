    private void renumerarIds() {
        int rowCount = model.getRowCount();
        for (int i = 0; i < rowCount; i++) {
            model.setValueAt(i + 1, i, 0); // Asignar IDs secuenciale
        }
    }

    private void actualizarArchivo() {
        int rowCount = model.getRowCount();
        List<String[]> categorias = new java.util.ArrayList<>();

        for (int i = 0; i < rowCount; i++) {
            String id = model.getValueAt(i, 0).toString();
            String nombre = model.getValueAt(i, 1).toString();
            String descripcion = model.getValueAt(i, 2).toString();
            categorias.add(new String[]{id, nombre, descripcion});
        }

        // Sobrescribir el archivo con las categorías actualizadas
        ProyectoAlgoritmos.sobrescribirArchivoConCategorias(categorias);
    }

