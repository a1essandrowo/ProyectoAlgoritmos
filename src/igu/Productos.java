    private void guardarbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarbtnActionPerformed
        // Verificar si hay datos en los campos de nombre y descripción
        if (nombreCategoriatxt.getText().isEmpty() || descrpCategoriastxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos.");
        } else {
            String id = String.valueOf(idCounter++);
            idCategoriatxt.setText(id); // Mostrar el ID en el campo de texto
            String nombre = nombreCategoriatxt.getText();
            String descripcion = descrpCategoriastxt.getText();

            // Agregar a la tabla
            model.addRow(new Object[]{id, nombre, descripcion});

            // Guardar en el archivo de texto
            ProyectoAlgoritmos.guardarCategoriaEnArchivo(id, nombre, descripcion);
        }
    }//GEN-LAST:event_guardarbtnActionPerformed

    private void CategoriastblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CategoriastblMouseClicked

        // Obtener el índice de la fila seleccionada
        selectedRow = Categoriastbl.getSelectedRow();
        if (selectedRow != -1) {
            // Obtener el ID de la columna 0 de la fila seleccionada
            String id = model.getValueAt(selectedRow, 0).toString();
            // Rellenar los campos de texto con los datos de la fila seleccionada
            idCategoriatxt.setText(id); // Mostrar el ID en el campo de texto
            nombreCategoriatxt.setText(model.getValueAt(selectedRow, 1).toString());
            descrpCategoriastxt.setText(model.getValueAt(selectedRow, 2).toString());
        }
    }//GEN-LAST:event_CategoriastblMouseClicked

    private void editarCategoriabtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarCategoriabtnActionPerformed
        // Verificar si hay una fila seleccionada para editar
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Seleccione una fila para editar.");
        } else {
            String nuevoNombre = nombreCategoriatxt.getText();
            String nuevaDescripcion = descrpCategoriastxt.getText();

            // Actualizar los datos en la tabla
            model.setValueAt(nuevoNombre, selectedRow, 1);
            model.setValueAt(nuevaDescripcion, selectedRow, 2);

            // Actualizar el archivo de texto
            actualizarArchivo();
            selectedRow = -1;
        }
    }//GEN-LAST:event_editarCategoriabtnActionPerformed

    private void eliminarbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarbtnActionPerformed
        // Verificar si hay una fila seleccionada para eliminar
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Seleccione una fila para eliminar.");
        } else {
            // Eliminar de la tabla
            model.removeRow(selectedRow);

            // Actualizar el archivo de texto
            actualizarArchivo();
            selectedRow = -1;
        }
    }//GEN-LAST:event_eliminarbtnActionPerformed

